package com.lavanderia.utp.model;

public class PrendaColor {
    
    int id;
    String color;
    
    public PrendaColor() {
    }

    public PrendaColor(String color) {
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
}
