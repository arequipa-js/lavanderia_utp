package com.lavanderia.utp.model;

public class Distrito {
    
    int id;
    String distrito;
    
    public Distrito() {
    }

    public Distrito(String distrito) {
        this.distrito = distrito;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDistrito() {
        return distrito;
    }

    public void setDistrito(String distrito) {
        this.distrito = distrito;
    }
    
}
